package learn.note.base;

public class Types {
    /**
     * 基本数据类型
     */
    int anInt = 10; // 四个字节
    float aFloat = 10.0f; // 四个字节
    double aDouble = 10.0D; // 八个字节
    short aShort = 10; // 两个字节
    char aChar = '王'; // 两个字节
    long aLong = 1000L; // 八个字节
    byte aByte = 0b1010001; // 一个字节
    boolean aBoolean = true; // 不重要！！！

    /**
     * 基础类型的封装类型
     */
    String string = "this is a string"; // 字符串类型
    // ......

}
